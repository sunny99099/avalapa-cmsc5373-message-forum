export class Message{
    //iinstance variables
    uid = null; //user id generated by firebase
    email = null;
    title = null;
    contents = null;
    timestamp = null;
    docId = null; //user id generated by firebase

    constructor(data){
        this.uid = data.uid;
        this.email = data.email;
        this.title = data.title;
        this.contents = data.contents;
        this.timestamp = data.timestamp;
    }

    setDocId(docId){
        this.docId = docId;
    }

    toFirestore(){
        return{
            uid: this.uid,
            email: this.email,
            title: this.title,
            contents: this.contents,
            timestamp: this.timestamp
        }
    }
}